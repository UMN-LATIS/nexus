	<h2 class="page-header">Adaptive mesh rendering</h2>

	<p>Nexus is a software package for creation and visualization of a batched multiresolution mesh structure.</p>
	<p>There is also a WebGL client available see <a href="http://web3d.duckdns.org">demo</a></p>

	<p>Main features of this library are: out-of-core interactive visualization, multiple instancing of models, http streaming, <b>compression</b>, color per vertex, point cloud support</p>

	<p>Future releases will provide: occlusion culling, projective textures using deferred shading,
collision detection, point splatting, etc. Read the <a href="roadmap.php">roadmap</a>
for more details</p>

